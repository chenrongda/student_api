package crd.student.api.reponse;

public class PassingNum {

    private String examName;

    private String examNum;

    private String mathNum;

    private String chineseNum;

    private String englishNum;

    public String getExamNum() {
        return examNum;
    }

    public void setExamNum(String examNum) {
        this.examNum = examNum;
    }

    public String getExamName() {
        return examName;
    }

    public void setExamName(String examName) {
        this.examName = examName;
    }

    public String getMathNum() {
        return mathNum;
    }

    public void setMathNum(String mathNum) {
        this.mathNum = mathNum;
    }

    public String getChineseNum() {
        return chineseNum;
    }

    public void setChineseNum(String chineseNum) {
        this.chineseNum = chineseNum;
    }

    public String getEnglishNum() {
        return englishNum;
    }

    public void setEnglishNum(String englishNum) {
        this.englishNum = englishNum;
    }
}
